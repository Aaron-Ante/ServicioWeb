/* Concatenacion de arreglos: crea dos arreglos de strings y luego concatenarlos en un solo arreglo */

const arreglo1 = ["Hola", "Mundo"];
const arreglo2 = ["Cómo", "estás"];

// Concatenar arreglos
const arregloConcatenado = arreglo1.concat(arreglo2);

// Mostrar el arreglo concatenado
console.log("Arreglo concatenado:", arregloConcatenado);
